
public class Inventario {
	//ATRIBUTOS

	public String tipo;
	public int cantidad;
	
	public void Inventario(String tipo, int cantidad){
	
		this.tipo= tipo;
		this.cantidad= cantidad;
		
		
	}
	public String tipo() {
        return tipo();
    }


    public void settipo(String tipo) {
        this.tipo = tipo;
    }


    public int getcantidad() {
        return cantidad;
    }


    public void setcantidad(int cantidad) {
        this.cantidad = cantidad;
        
    }
}
